/**
 * Variables in JS 
 * We can create a variable in js using a var keyword
 */

// variable declaration
var container ;
console.log(container); // undefined
console.log(typeof container); // It will tell me what type of data my container is storing

//Variable initialization
container = 10;
console.log(container); // 10
console.log(typeof container);

container = 'anshu';
console.log(container); // anshu
console.log(typeof container);

container = false;
console.log(container); //false
console.log(typeof container);